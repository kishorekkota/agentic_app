def is_valid_us_state(state):
    """
    Check if the input is a valid US state name or abbreviation.

    Args:
        state (str): The state name or abbreviation to validate.

    Returns:
        bool: True if the input is a valid US state name or abbreviation, False otherwise.
    """
    state = state.strip().title()
    return state in US_STATES.values() or state in US_STATES.keys()